// @desc    Get reorder statistics
// @route   GET /api/reorders/stats
// @access  Private (Inventory/Admin only)
const getReorderStats = asyncHandler(async (req, res) => {
  const totalReorders = await Reorder.countDocuments();
  const pendingReorders = await Reorder.countDocuments({ status: 'pending' });
  const approvedReorders = await Reorder.countDocuments({ status: 'approved' });
  const rejectedReorders = await Reorder.countDocuments({ status: 'rejected' });
  const orderedReorders = await Reorder.countDocuments({ status: 'ordered' });
  const autoGeneratedReorders = await Reorder.countDocuments({ isAutoGenerated: true });

  const statusStats = await Reorder.aggregate([
    { $group: { _id: '$status', count: { $sum: 1 } } }
  ]);

  const reasonStats = await Reorder.aggregate([
    { $group: { _id: '$reason', count: { $sum: 1 } } }
  ]);

  const priorityStats = await Reorder.aggregate([
    { $group: { _id: '$priority', count: { $sum: 1 } } }
  ]);

  const monthlyStats = await Reorder.aggregate([
    {
      $group: {
        _id: {
          year: { $year: '$createdAt' },
          month: { $month: '$createdAt' }
        },
        count: { $sum: 1 }
      }
    },
    { $sort: { '_id.year': -1, '_id.month': -1 } },
    { $limit: 12 }
  ]);

  res.json({
    success: true,
    data: {
      total: totalReorders,
      pending: pendingReorders,
      approved: approvedReorders,
      rejected: rejectedReorders,
      ordered: orderedReorders,
      autoGenerated: autoGeneratedReorders,
      byStatus: statusStats,
      byReason: reasonStats,
      byPriority: priorityStats,
      monthly: monthlyStats
    }
  });
});

