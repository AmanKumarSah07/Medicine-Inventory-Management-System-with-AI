import express from 'express';
import Reorder from '../models/Reorder.js';
import Medicine from '../models/Medicine.js';
import Order from '../models/Order.js';
import ActivityLog from '../models/ActivityLog.js';
import { authenticate, inventoryOrAdmin } from '../middleware/auth.js';
import { validateReorder, validateObjectId, validateQuery } from '../middleware/validation.js';
import { asyncHandler } from '../middleware/errorHandler.js';

const router = express.Router();

// @desc    Get all reorders
// @route   GET /api/reorders
// @access  Private (Inventory/Admin only)
const getReorders = asyncHandler(async (req, res) => {
  const { 
    page = 1, 
    limit = 10, 
    status, 
    reason, 
    priority,
    medicine,
    sort = 'createdAt',
    order = 'desc'
  } = req.query;
  
  // Build query
  const query = {};
  
  if (status) {
    query.status = status;
  }
  
  if (reason) {
    query.reason = reason;
  }
  
  if (priority) {
    query.priority = priority;
  }
  
  if (medicine) {
    query.medicine = medicine;
  }

  const sortOrder = order === 'desc' ? -1 : 1;
  const sortObj = { [sort]: sortOrder };

  const options = {
    page: parseInt(page),
    limit: parseInt(limit),
    sort: sortObj,
    populate: 'medicine triggeredBy approvedBy rejectedBy order'
  };

  const reorders = await Reorder.paginate(query, options);

  res.json({
    success: true,
    data: reorders
  });
});

// @desc    Get single reorder
// @route   GET /api/reorders/:id
// @access  Private (Inventory/Admin only)
const getReorder = asyncHandler(async (req, res) => {
  const reorder = await Reorder.findById(req.params.id)
    .populate('medicine triggeredBy approvedBy rejectedBy order');
  
  if (!reorder) {
    return res.status(404).json({
      success: false,
      message: 'Reorder not found'
    });
  }

  res.json({
    success: true,
    data: { reorder }
  });
});

// @desc    Create reorder
// @route   POST /api/reorders
// @access  Private (Inventory/Admin only)
const createReorder = asyncHandler(async (req, res) => {
  const { medicine, suggestedQuantity, reason, priority, notes } = req.body;

  // Get medicine details
  const medicineDoc = await Medicine.findById(medicine);
  if (!medicineDoc) {
    return res.status(404).json({
      success: false,
      message: 'Medicine not found'
    });
  }

  const reorder = await Reorder.create({
    medicine,
    suggestedQuantity,
    currentQuantity: medicineDoc.quantity,
    reorderLevel: medicineDoc.reorderLevel,
    reason,
    priority,
    notes,
    triggeredBy: req.user._id,
    status: 'pending',
    isAutoGenerated: false
  });

  // Log reorder creation
  await ActivityLog.createLog({
    action: 'reorder_suggested',
    entityType: 'reorder',
    entityId: reorder._id,
    userId: req.user._id,
    userRole: req.user.role,
    ipAddress: req.ip,
    userAgent: req.get('User-Agent'),
    severity: 'info',
    category: 'inventory',
    message: `Reorder suggested for ${medicineDoc.name} by ${req.user.name}`,
    details: { 
      medicine: medicineDoc.name,
      suggestedQuantity: reorder.suggestedQuantity,
      reason: reorder.reason,
      priority: reorder.priority
    }
  });

  res.status(201).json({
    success: true,
    message: 'Reorder created successfully',
    data: { reorder }
  });
});

// @desc    Update reorder
// @route   PUT /api/reorders/:id
// @access  Private (Inventory/Admin only)
const updateReorder = asyncHandler(async (req, res) => {
  const reorder = await Reorder.findById(req.params.id);
  
  if (!reorder) {
    return res.status(404).json({
      success: false,
      message: 'Reorder not found'
    });
  }

  // Only allow updates if reorder is pending
  if (reorder.status !== 'pending') {
    return res.status(400).json({
      success: false,
      message: 'Only pending reorders can be updated'
    });
  }

  const oldValues = {
    suggestedQuantity: reorder.suggestedQuantity,
    priority: reorder.priority,
    reason: reorder.reason,
    notes: reorder.notes
  };

  const updatedReorder = await Reorder.findByIdAndUpdate(
    req.params.id,
    req.body,
    { new: true, runValidators: true }
  ).populate('medicine triggeredBy approvedBy rejectedBy order');

  // Log reorder update
  await ActivityLog.createLog({
    action: 'reorder_updated',
    entityType: 'reorder',
    entityId: reorder._id,
    userId: req.user._id,
    userRole: req.user.role,
    ipAddress: req.ip,
    userAgent: req.get('User-Agent'),
    severity: 'info',
    category: 'inventory',
    message: `Reorder updated by ${req.user.name}`,
    details: { oldValues, newValues: req.body }
  });

  res.json({
    success: true,
    message: 'Reorder updated successfully',
    data: { reorder: updatedReorder }
  });
});

// @desc    Approve reorder
// @route   PUT /api/reorders/:id/approve
// @access  Private (Inventory/Admin only)
const approveReorder = asyncHandler(async (req, res) => {
  const { notes } = req.body;
  
  const reorder = await Reorder.findById(req.params.id);
  
  if (!reorder) {
    return res.status(404).json({
      success: false,
      message: 'Reorder not found'
    });
  }

  if (reorder.status !== 'pending') {
    return res.status(400).json({
      success: false,
      message: 'Only pending reorders can be approved'
    });
  }

  await reorder.approve(req.user._id, notes);

  // Create order from approved reorder
  const medicine = await Medicine.findById(reorder.medicine);
  const order = await Order.create({
    medicine: reorder.medicine,
    supplier: medicine.supplier || null,
    quantity: reorder.suggestedQuantity,
    unitPrice: medicine.cost || 0,
    totalPrice: (medicine.cost || 0) * reorder.suggestedQuantity,
    priority: reorder.priority,
    requestedBy: req.user._id,
    status: 'pending',
    notes: `Auto-generated from reorder ${reorder._id}`
  });

  // Mark reorder as ordered
  await reorder.markAsOrdered(order._id, req.user._id, 'Order created from approved reorder');

  // Log reorder approval
  await ActivityLog.createLog({
    action: 'reorder_approved',
    entityType: 'reorder',
    entityId: reorder._id,
    userId: req.user._id,
    userRole: req.user.role,
    ipAddress: req.ip,
    userAgent: req.get('User-Agent'),
    severity: 'info',
    category: 'inventory',
    message: `Reorder approved by ${req.user.name}`,
    details: { 
      orderId: order._id,
      orderNumber: order.orderNumber,
      notes
    }
  });

  res.json({
    success: true,
    message: 'Reorder approved successfully',
    data: { 
      reorder: await Reorder.findById(req.params.id).populate('medicine triggeredBy order'),
      order
    }
  });
});

// @desc    Reject reorder
// @route   PUT /api/reorders/:id/reject
// @access  Private (Inventory/Admin only)
const rejectReorder = asyncHandler(async (req, res) => {
  const { reason, notes } = req.body;
  
  const reorder = await Reorder.findById(req.params.id);
  
  if (!reorder) {
    return res.status(404).json({
      success: false,
      message: 'Reorder not found'
    });
  }

  if (reorder.status !== 'pending') {
    return res.status(400).json({
      success: false,
      message: 'Only pending reorders can be rejected'
    });
  }

  await reorder.reject(req.user._id, reason, notes);

  // Log reorder rejection
  await ActivityLog.createLog({
    action: 'reorder_rejected',
    entityType: 'reorder',
    entityId: reorder._id,
    userId: req.user._id,
    userRole: req.user.role,
    ipAddress: req.ip,
    userAgent: req.get('User-Agent'),
    severity: 'warning',
    category: 'inventory',
    message: `Reorder rejected by ${req.user.name}`,
    details: { reason, notes }
  });

  res.json({
    success: true,
    message: 'Reorder rejected successfully',
    data: { reorder: await Reorder.findById(req.params.id).populate('medicine triggeredBy') }
  });
});

